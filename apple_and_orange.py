def countApplesAndOranges(s, t, a, b, apples, oranges):
    # Write your code here
    cnt_apple = 0
    cnt_orange = 0
    
    for aa in apples:
        if((aa + a) in range(s, t + 1)):
            cnt_apple = cnt_apple + 1
    
    for oo in oranges:
        if((oo + b) in range(s, t + 1)):
            cnt_orange = cnt_orange + 1
    
    print(cnt_apple)
    print(cnt_orange)
    

